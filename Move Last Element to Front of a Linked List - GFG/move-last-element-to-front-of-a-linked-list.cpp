class ListNode{
public:
    int val;
    ListNode *next;
    ListNode(int x){
        val=x;
        next=NULL;
    }
};


// } Driver Code Ends
//User function Template for C++

class Solution{
public:
    ListNode *moveToFront(ListNode *head) {

  if (head == NULL || head->next == NULL) {
    return head;
  }

  ListNode *secondLast = head; // will point to second last element after loop
  while (secondLast->next->next != NULL) {
    secondLast = secondLast->next;
  }

  ListNode *last = secondLast->next;
  last->next = head;
  secondLast->next = NULL;

  return last; // last is now head
}
};

