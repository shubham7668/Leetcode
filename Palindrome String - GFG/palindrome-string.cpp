//User function template for C++
class Solution{
public:	
	
	
	int isPalindrome(string s)
	{
	    int j=s.size()-1;
	    for(int i=0;i<s.size();i++)
	    {
	        if(s[i]!=s[j]) return 0;
	        j--;
	        if(i==j) return 1;
	    }
	    
	}

};

